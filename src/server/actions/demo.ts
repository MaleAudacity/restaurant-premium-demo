async function getRestaurantOrThrow() {
  if (!(await isDatabaseReachable())) {
    throw new Error(
      "Database is unavailable. Start PostgreSQL and run db:push/db:seed before using demo control actions.",
    );
  }

  const restaurant = await prisma.restaurant.findUnique({
    where: {
      slug: DEFAULT_RESTAURANT_SLUG,
    },
  });

  if (!restaurant) {
    throw new Error("Mirch Masala restaurant not found. Seed the database first.");
  }

  return restaurant;
}

async function getAnyCustomer(restaurantId: string) {
  const customer = await prisma.customer.findFirst({
    where: { restaurantId },
    orderBy: { createdAt: "asc" },
  });

  if (customer) {
    return customer;
  }

  return prisma.customer.create({
    data: {
      restaurantId,
      firstName: "Demo",
      lastName: "Guest",
      phone: "+91 90000 19999",
      email: "demo.guest@example.com",
      notes: "Autogenerated fallback customer.",
    },
  });
}

function stampOrderNumber() {
  return `MM-${Date.now().toString().slice(-6)}`;
}

function stampReference(prefix: string) {
  return `${prefix}-${Date.now().toString().slice(-5)}`;
}

