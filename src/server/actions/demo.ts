async function createConversationFlow({
  summary,
  messages,
  createRecord,
}: {
  summary: string;
  messages: string[];
  createRecord?: (restaurantId: string, customerId: string) => Promise<void>;
}) {
  const restaurant = await getRestaurantOrThrow();
  const customer = await getAnyCustomer(restaurant.id);

  const conversation = await prisma.conversation.create({
    data: {
      restaurantId: restaurant.id,
      customerId: customer.id,
      channel: ConversationChannel.WHATSAPP,
      status: ConversationStatus.OPEN,
      summary,
      lastMessageAt: new Date(),
    },
  });

  for (const [index, message] of messages.entries()) {
    await prisma.messageLog.create({
      data: {
        restaurantId: restaurant.id,
        conversationId: conversation.id,
        direction:
          index % 2 === 0 ? MessageDirection.INBOUND : MessageDirection.OUTBOUND,
        content: message,
      },
    });
  }

  if (createRecord) {
    await createRecord(restaurant.id, customer.id);
  }

  await refreshAll();
  emitRealtimeEvent({
    type: "conversation.updated",
    entity: "conversation",
    entityId: conversation.id,
  });
}

